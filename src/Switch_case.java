public class Switch_case {
    public static void main(String args[]){
        int day=2;

        switch(day){
            case 1:
                System.out.println("Monday");
                break;
            case 2:
                System.out.println("Tuesday");
                break;
            case 3:
                System.out.println("Tuesday");
                break;

        }
        int a=10;
        int b=2;
        System.out.println((a>b)?"a is bigger":"b is bigger");
    }
}
