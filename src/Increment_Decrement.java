public class Increment_Decrement {
    public static void main(String args[]){
        int a=10;
        System.out.println("a value before incrementing :"+a);
        a++;
        System.out.println("a value after incrementing :"+a);
        ++a;
        System.out.println("a pre incrementing :"+a);

         int b=2;
        System.out.println("b value  :"+b);
        --b;
        System.out.println("b value pre decrementing :"+b);
        b--;
        System.out.println("b value post decrementing :"+b);




    }
}
